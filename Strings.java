package Javapro;

public class Strings {

	public static void main(String[] args) {
		String name1="A";
		String name2="B";
		String name3=name1+name2; //Concatenate String
		System.out.println("Concatenate String:"+name3);
		
		// CharAt

		String name="DikshaRamos";
		System.out.println(name.charAt(5)); //Showing indexed no
		
		//Length
		System.out.println(name.length());
		
		//Substring
         		
		String name5="Dikshant Banage";
		System.out.println(name5.substring(0, 8));
		
	}

}
