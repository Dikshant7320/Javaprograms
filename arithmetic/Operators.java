package arithmetic;

public class Operators {
	 public static void main(String[]args) {
      

		 int a=12;  // increment (++)operator
		 
		 System.out.println(a);
		 
		 ++a;
		 System.out.println(a);
		 
		 ++a;
		 System.out.println(a);
		 
		 --a;     //decrement(--)operator
		 System.out.println(a);
		 
		 
	 }
}
